$lenguaje["acerca_de"]="El Sistema Integrado de Información (SII) sirve de apoyo en el proyecto: Manejo integrado y sostenible de los recursos hídricos transfronterizos de la cuenca del río Amazonas considerando la variabilidad y el cambio climático. Este sistema mostrará la herramienta Monitoreo de Calidad de Agua el cual plasma en mapas georeferenciados la investigación referente a la contaminación del agua de la Cuenca Amazónica debido a los impactos negativos en la salud humana y ambiental causados por la intensificación del uso de sustancias contaminadas orgánicas e inorgánicas, ocurrida en los últimos cuarenta años, toda esta información se muestra en su visor respectivo llamado Visor de Calidad de agua además detalla las variables que fueron identificadas en la investigación. Este sistema también muestra la herramienta Atlas de la Vulnerabilidad Hidroclimática la cual muestra a las capas según temáticas relacionadas con eventos climáticos extremos y sus impactos sobre los recursos hídricos dentro de la cuenca amazónica basadas en información bibliográfica documentada y cartográfica mediante los Puntos Focales de los países de la OTCA, asi como de consultas elaboradas a través la web y otras fuentes de información oficiales disponibles y de acceso público en algunos países.El Sistema Integrado de Información también muestra el Sistema de Alerta Temprana, un Atlas Botánico, Legislación y Documentos, toda esta información se basa en los estudios de los ocho países que pertenecen a la Organización del Tratado de Cooperación Amazónica: Bolivia, Brasil, Colombia, Ecuador, Guyana, Suriname, Perú y Venezuela.";